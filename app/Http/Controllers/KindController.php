    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $kind = Kind::findOrFail($id);
        $kind->delete();

        return redirect()->back()->with('success','Data berhasil Dihapus!');
